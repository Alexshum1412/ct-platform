async function main() {
  console.log('📚 Генерирую теоретические статьи...\n');

  // Get all topics with their subject
  const topics = await prisma.topic.findMany({
    include: { subject: true },
  });

  // Existing theory titles
  const existingTheory = await prisma.theory.findMany({ select: { title: true, topicId: true } });
  const existingMap = new Set(existingTheory.map(t => `${t.topicId}-${t.title}`));

  let created = 0;
  let skipped = 0;

  for (const topic of topics) {
    const key = `${topic.id}-${topic.name}`;
    if (existingMap.has(key)) { skipped++; continue; }

    // Match rich theory by sub-topic name match
    const subtopics = await prisma.subtopic.findMany({ where: { topicId: topic.id } });

    let theoryContent;
    // Check if any subtopic has rich theory
    const richMatch = subtopics.find(s => RICH_THEORY[s.name]);
    if (richMatch) {
      theoryContent = RICH_THEORY[richMatch.name];
    } else {
      theoryContent = getStubTheory(topic.name, topic.subject.name);
    }

    try {
      await prisma.theory.create({
        data: {
          subjectId: topic.subjectId,
          topicId: topic.id,
          title: theoryContent.title,
          content: theoryContent.content,
          tags: JSON.stringify(theoryContent.tags),
          status: 'ACTIVE',
          order: topic.order,
        },
      });
      created++;
    } catch (e) {
      console.error('Error:', e.message.substring(0, 60));
    }
  }

  console.log(`✅ Создано ${created} статей теории (пропущено существующих: ${skipped})\n`);

  // Now also create theory for sub-topics with rich content
  console.log('📖 Дополнительные статьи по под-темам...\n');
  let extraCount = 0;
  for (const [subtopicName, theoryData] of Object.entries(RICH_THEORY)) {
    const subtopic = await prisma.subtopic.findFirst({
      where: { name: subtopicName },
      include: { topic: true },
    });
    if (!subtopic) continue;

    const exists = await prisma.theory.findFirst({
      where: { subtopicId: subtopic.id, title: theoryData.title },
    });
    if (exists) continue;

    try {
      await prisma.theory.create({
        data: {
          subjectId: subtopic.topic.subjectId,
          topicId: subtopic.topicId,
          subtopicId: subtopic.id,
          title: theoryData.title,
          content: theoryData.content,
          tags: JSON.stringify(theoryData.tags),
          status: 'ACTIVE',
          order: subtopic.order,
        },
      });
      extraCount++;
    } catch (e) {
      console.error('Sub error:', e.message.substring(0, 60));
    }
  }
  console.log(`✅ Создано ${extraCount} статей по под-темам\n`);

  const totalTheory = await prisma.theory.count();
  console.log(`📊 Всего теоретических статей в БД: ${totalTheory}`);
}

main().catch(console.error).finally(() => prisma.$disconnect());
