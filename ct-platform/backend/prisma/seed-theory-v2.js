function findRichMatch(subtopicName) {
  for (const key of Object.keys(MATH_THEORY)) {
    if (subtopicName.toLowerCase().includes(key.toLowerCase()) ||
        key.toLowerCase().includes(subtopicName.toLowerCase().split(/[\s,.:()«»\/—\-]/)[0])) {
      return MATH_THEORY[key];
    }
  }
  return null;
}

async function main() {
  console.log('📚 Theory v2 — удаление старого и генерация по подтемам...\n');

  // Step 1: Delete old theory (it's misallocated)
  const deleted = await prisma.theory.deleteMany({});
  console.log(`🗑️  Удалено ${deleted.count} старых статей\n`);

  // Step 2: For each subtopic, create a theory article strictly linked to subject/topic/subtopic
  const subtopics = await prisma.subtopic.findMany({
    include: { topic: { include: { subject: true } } },
    orderBy: { order: 'asc' },
  });

  let richCount = 0;
  let stubCount = 0;

  for (const sub of subtopics) {
    const rich = findRichMatch(sub.name);
    const data = rich ?? getStubTheory(sub.name, sub.topic.name, sub.topic.subject.name);

    try {
      await prisma.theory.create({
        data: {
          subjectId: sub.topic.subjectId,
          topicId: sub.topicId,
          subtopicId: sub.id,
          title: data.title,
          content: data.content,
          tags: JSON.stringify(data.tags ?? []),
          status: 'ACTIVE',
          order: sub.order,
        },
      });
      if (rich) richCount++; else stubCount++;
    } catch (e) {
      console.error(`✗ ${sub.name}:`, e.message.substring(0, 60));
    }
  }

  console.log(`✅ Создано ${richCount + stubCount} статей (богатых: ${richCount}, заглушек: ${stubCount})\n`);

  const total = await prisma.theory.count();
  const withSubtopic = await prisma.theory.count({ where: { subtopicId: { not: null } } });
  console.log(`📊 Всего: ${total}, с привязкой к подтеме: ${withSubtopic}`);
}

main().catch(console.error).finally(() => prisma.$disconnect());
