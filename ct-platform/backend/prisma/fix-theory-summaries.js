function curatedFor(title, topicName, slug) {
  const ov = OVERRIDES.find((o) => o.re.test(title + ' ' + topicName));
  if (ov) return DATA[ov.key];
  return TOPIC[topicName] || SUBJECT_DEFAULT[slug] || null;
}

async function main() {
  const subjects = await prisma.subject.findMany({ select: { id: true, slug: true } });
  const slugById = new Map(subjects.map((s) => [s.id, s.slug]));
  const topics = await prisma.topic.findMany({ select: { id: true, name: true } });
  const topicName = new Map(topics.map((t) => [t.id, t.name]));
  const theory = await prisma.theory.findMany();

  let fromContent = 0, fromFallback = 0, mtSet = 0, mtCleared = 0;
  for (const th of theory) {
    const tn = topicName.get(th.topicId) || '';
    const slug = slugById.get(th.subjectId) || '';
    const cur = curatedFor(th.title, tn, slug);
    // Подтемно-специфичный override (узкий regex) — единственный безопасный
    // курированный summary; темо-уровневые (TOPIC) для summary НЕ используем,
    // иначе «Магнитное поле» снова получит summary закона Ома.
    const ov = OVERRIDES.find((o) => o.re.test(th.title + ' ' + tn));
    // Авто-сгенерированные «заглушки» (общий шаблон) — реального содержания нет.
    const isStub = /Подтема раздела/.test(th.content || '');

    let summary = isStub ? '' : contentSummary(th.content);
    if (summary.length < 40) {
      summary = (ov ? DATA[ov.key].summary : '') || `Ключевые определения, формулы и алгоритмы по теме «${th.title}».`;
      fromFallback++;
    } else fromContent++;

    const commonMistakes = cur ? JSON.stringify(cur.commonMistakes) : null;
    const examTraps = cur ? JSON.stringify(cur.examTraps) : null;
    if (cur) mtSet++; else mtCleared++;

    if (!DRY) {
      await prisma.theory.update({ where: { id: th.id }, data: { summary, commonMistakes, examTraps } });
    }
  }
  console.log(`Theory: ${theory.length}`);
  console.log(`summary: из контента ${fromContent}, из выверенного фолбэка ${fromFallback}`);
  console.log(`mistakes/traps: назначено ${mtSet}, очищено (нет совпадения) ${mtCleared}`);
  console.log(DRY ? '[DRY] не применено' : '✓ применено');
}
main().catch((e) => { console.error(e); process.exit(1); }).finally(() => prisma.$disconnect());
