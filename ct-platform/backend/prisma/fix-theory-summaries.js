// чтобы такие статьи ушли в фолбэк, а не давали бессмысленное summary.
const BOILERPLATE = /Подтема раздела|Изучите основные определения|входит в (программу|курс)|Эта тема входит|Основные определения и термины|Типовые формулы и правила|Алгоритмы решения|Связь с другими подтемами|Прочитайте основные положения|Ключевые понятия и определения/i;
// Сохраняем $...$ и **жирный** (их рендерит MathFormula); убираем только # ` > и маркеры.
function stripMd(s) {
  return s.replace(/[`#>]/g, '').replace(/\s+/g, ' ').trim();
}
function contentSummary(content) {
  if (!content) return '';
  const noBlock = content.replace(/\$\$[\s\S]*?\$\$/g, ' '); // убрать блочные формулы
  const parts = [];
  for (const raw of noBlock.split('\n')) {
    let l = raw.trim();
    if (!l) continue;
    if (/^#{1,6}\s/.test(l)) continue;            // заголовки
    if (/^\|/.test(l) || /^[-:|\s]+$/.test(l)) continue; // таблицы/разделители
    l = l.replace(/^[-*>]\s+/, '').replace(/^\d+[.)]\s+/, ''); // маркеры списка
    const clean = stripMd(l);
    if (clean.replace(/[$*_^\\]/g, '').trim().length < 12) continue; // мало «настоящего» текста
    if (BOILERPLATE.test(clean)) continue;        // шаблонный текст
    parts.push(clean);
    if (parts.join(' ').length >= 130) break;
  }
  let s = parts.join(' ').replace(/\s+/g, ' ').trim();
  if (s.length > 210) s = s.slice(0, 207).replace(/\s+\S*$/, '') + '…';
  return s;
}

