async function main() {
  console.log('🌳 Создаю под-темы для расширенной иерархии...');

  let totalCreated = 0;
  for (const [subjectSlug, topicsMap] of Object.entries(subtopicsData)) {
    const subject = await prisma.subject.findFirst({ where: { slug: subjectSlug } });
    if (!subject) continue;

    let subjectCount = 0;
    for (const [topicName, subtopics] of Object.entries(topicsMap)) {
      const topic = await prisma.topic.findFirst({ where: { subjectId: subject.id, name: topicName } });
      if (!topic) continue;

      // Delete existing subtopics for this topic to avoid duplicates
      await prisma.subtopic.deleteMany({ where: { topicId: topic.id } });

      let order = 1;
      for (const subtopicName of subtopics) {
        try {
          await prisma.subtopic.create({
            data: {
              topicId: topic.id,
              name: subtopicName,
              description: `Подтема раздела "${topicName}"`,
              order: order++,
            },
          });
          subjectCount++;
          totalCreated++;
        } catch (e) {
          console.error(`  ✗ ${subtopicName}:`, e.message.substring(0, 60));
        }
      }
    }
    console.log(`✅ ${subject.name}: +${subjectCount} под-тем`);
  }

  console.log(`\n🎉 Создано ${totalCreated} под-тем`);
}

main().catch(console.error).finally(() => prisma.$disconnect());
