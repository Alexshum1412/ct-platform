function pickRules(slug) {
  if (slug === 'math') return MATH_RULES;
  if (slug === 'physics') return PHYS_RULES;
  if (slug === 'chemistry') return CHEM_RULES;
  return null;
}

function curated(slug, topicName, hay) {
  const arr = pickRules(slug);
  if (arr) {
    const hit = arr.find((x) => x.re.test(hay));
    if (hit) return { m: hit.m, t: hit.t };
    // STEM без точного совпадения — оставим пусто (лучше, чем чужое)
    return null;
  }
  if (slug === 'geography') return { m: GEO_DEFAULT.m, t: GEO_DEFAULT.t };
  if (slug === 'history' || slug === 'world-history') return { m: HIST_DEFAULT.m, t: HIST_DEFAULT.t };
  const tr = TOPIC_RULES[topicName];
  return tr ? { m: tr.m, t: tr.t } : null;
}

async function main() {
  const subjects = await prisma.subject.findMany({ select: { id: true, slug: true } });
  const slugById = new Map(subjects.map((s) => [s.id, s.slug]));
  const topics = await prisma.topic.findMany({ select: { id: true, name: true } });
  const topicName = new Map(topics.map((t) => [t.id, t.name]));
  const subs = await prisma.subtopic.findMany({ select: { id: true, name: true } });
  const subName = new Map(subs.map((s) => [s.id, s.name]));
  const theory = await prisma.theory.findMany({ select: { id: true, title: true, subjectId: true, topicId: true, subtopicId: true } });

  let set = 0, cleared = 0;
  const unmatchedStem = [];
  for (const th of theory) {
    const slug = slugById.get(th.subjectId) || '';
    const tn = topicName.get(th.topicId) || '';
    const sn = subName.get(th.subtopicId) || '';
    const hay = `${sn} | ${th.title} | ${tn}`;
    const cur = curated(slug, tn, hay);

    if (cur) set++; else {
      cleared++;
      if (['math', 'physics', 'chemistry'].includes(slug)) unmatchedStem.push(`${slug}: ${sn || th.title}`);
    }
    if (VERIFY && ['math', 'physics', 'chemistry'].includes(slug)) {
      console.log(`[${slug}] ${sn || th.title}\n      → ${cur ? cur.m[0] : '∅ (очищено)'}`);
    }

    if (!DRY) {
      await prisma.theory.update({
        where: { id: th.id },
        data: {
          commonMistakes: cur ? JSON.stringify(cur.m) : null,
          examTraps: cur ? JSON.stringify(cur.t) : null,
        },
      });
    }
  }

  console.log(`Theory: ${theory.length}`);
  console.log(`mistakes/traps: назначено ${set}, очищено (нет совпадения) ${cleared}`);
  if (REPORT && unmatchedStem.length) {
    console.log(`\n⚠ STEM без точного правила (${unmatchedStem.length}):`);
    unmatchedStem.forEach((u) => console.log('   - ' + u));
  }
  console.log(DRY ? '[DRY] не применено' : '✓ применено');
}
main().catch((e) => { console.error(e); process.exit(1); }).finally(() => prisma.$disconnect());
