async function main() {
  console.log('🌲 Создаю полную иерархию из ТЕМЫРАЗДЕЛЫ.md...\n');

  let totalTopics = 0;
  let totalSubtopics = 0;

  for (const [subjectSlug, topics] of Object.entries(HIERARCHY)) {
    const subject = await prisma.subject.findFirst({ where: { slug: subjectSlug } });
    if (!subject) { console.log(`⚠️ Subject ${subjectSlug} not found`); continue; }

    console.log(`📚 ${subject.name}:`);

    // SAFE: detach questions from old topics (set topicId to null), then delete old topics
    const oldTopics = await prisma.topic.findMany({ where: { subjectId: subject.id }, select: { id: true } });
    if (oldTopics.length > 0) {
      const oldIds = oldTopics.map(t => t.id);
      // Detach questions and theory from old topics
      await prisma.question.updateMany({ where: { topicId: { in: oldIds } }, data: { topicId: null } });
      await prisma.theory.updateMany({ where: { topicId: { in: oldIds } }, data: { topicId: null } });
      // Delete subtopics first (cascade), then topics
      await prisma.subtopic.deleteMany({ where: { topicId: { in: oldIds } } });
      await prisma.topic.deleteMany({ where: { subjectId: subject.id } });
    }

    let order = 1;
    for (const [topicName, subtopicsList] of Object.entries(topics)) {
      const topic = await prisma.topic.create({
        data: {
          subjectId: subject.id,
          name: topicName,
          description: `Раздел "${topicName}" — официальная программа ЦТ/ЦЭ`,
          order: order++,
        },
      });
      totalTopics++;

      let subOrder = 1;
      for (const subtopicName of subtopicsList) {
        try {
          await prisma.subtopic.create({
            data: {
              topicId: topic.id,
              name: subtopicName,
              description: `Подтема раздела "${topicName}"`,
              order: subOrder++,
            },
          });
          totalSubtopics++;
        } catch (e) {
          console.error(`  ✗ ${subtopicName}:`, e.message.substring(0, 50));
        }
      }
      console.log(`  ✓ ${topicName} (${subtopicsList.length} под-тем)`);
    }

    await prisma.subject.update({
      where: { id: subject.id },
      data: { topicsCount: Object.keys(topics).length },
    });

    // Re-link orphaned questions to new topics by section match (best-effort)
    const newTopics = await prisma.topic.findMany({ where: { subjectId: subject.id } });
    let relinked = 0;
    for (const newTopic of newTopics) {
      // Match orphans whose section contains key word from topic name
      const keyword = newTopic.name.split(' ')[0];
      const r = await prisma.question.updateMany({
        where: {
          subjectId: subject.id,
          topicId: null,
          OR: [
            { section: { contains: keyword } },
            { section: { contains: newTopic.name } },
          ],
        },
        data: { topicId: newTopic.id },
      });
      relinked += r.count;
    }
    // Fallback: any remaining orphans get the first topic
    if (newTopics.length > 0) {
      const r = await prisma.question.updateMany({
        where: { subjectId: subject.id, topicId: null },
        data: { topicId: newTopics[0].id },
      });
      relinked += r.count;
    }
    console.log(`  → перепривязано ${relinked} заданий\n`);
  }

  console.log(`✅ Создано ${totalTopics} тем и ${totalSubtopics} под-тем\n`);
}

main().catch(console.error).finally(() => prisma.$disconnect());
