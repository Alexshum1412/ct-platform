async function main() {
  const subjects = await prisma.subject.findMany({ select: { id: true, slug: true } });
  const bySlug = new Map(subjects.map(s => [s.slug, s.id]));

  let created = 0, updated = 0, skipped = 0;
  for (const p of PROBLEMS) {
    const subjectId = bySlug.get(p.slug);
    if (!subjectId) { console.warn(`! предмет не найден: ${p.slug} (задача ${p.title})`); skipped++; continue; }
    const externalId = `OLY-${p.slug}-${String(p.n).padStart(2, '0')}`;
    const data = {
      subjectId,
      title: p.title,
      content: p.content,
      answer: p.answer,
      answerType: 'TEXT',
      solution: p.solution,
      hints: JSON.stringify(p.hints),
      level: p.level,
      difficulty: p.difficulty,
      topic: p.topic,
      grade: '9–11',
      year: p.year,
      points: PTS[p.level],
      tags: JSON.stringify(p.tags),
      source: SOURCE,
      status: 'ACTIVE',
    };
    const existing = await prisma.olympiadProblem.findUnique({ where: { externalId } });
    if (existing) {
      await prisma.olympiadProblem.update({ where: { externalId }, data });
      updated++;
    } else {
      await prisma.olympiadProblem.create({ data: { ...data, externalId } });
      created++;
    }
  }

  console.log(`Олимпиадные задачи (волна 2): создано ${created}, обновлено ${updated}, пропущено ${skipped}.`);
  const total = await prisma.olympiadProblem.count();
  console.log(`Всего задач в банке: ${total}.`);
}

main()
  .catch((e) => { console.error(e); process.exitCode = 1; })
  .finally(() => prisma.$disconnect());
