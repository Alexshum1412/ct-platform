function rngShuffle(arr, seed) {
  let a = 2166136261 ^ seed;
  const rnd = () => { a = Math.imul(a ^ (a >>> 15), 1 | a); a = (a + Math.imul(a ^ (a >>> 7), 61 | a)) ^ a; return ((a ^ (a >>> 14)) >>> 0) / 4294967296; };
  const r = arr.slice();
  for (let i = r.length - 1; i > 0; i--) { const j = Math.floor(rnd() * (i + 1)); [r[i], r[j]] = [r[j], r[i]]; }
  return r;
}
function tagsFrom(name) { return name.toLowerCase().replace(/[^a-zа-яё0-9 ]/gi, ' ').split(/\s+/).filter(w => w.length > 4).slice(0, 3); }

async function main() {
  const existing = await prisma.question.findMany({ where: { externalId: { startsWith: 'HUM-' } }, select: { externalId: true } });
  const have = new Set(existing.map(e => e.externalId));
  let created = 0, skippedNoSubject = 0;
  const perSubject = {};

  for (const [slug, items] of Object.entries(BANK)) {
    const subject = await prisma.subject.findUnique({ where: { slug } });
    if (!subject) { skippedNoSubject++; continue; }
    const topics = await prisma.topic.findMany({ where: { subjectId: subject.id }, orderBy: { order: 'asc' } });
    perSubject[slug] = 0;
    for (let n = 0; n < items.length; n++) {
      const [kw, content, opts, correctIdx, expl, diff] = items[n];
      const extId = `HUM-${slug}-${n + 1}`;
      if (have.has(extId)) continue;
      // pick topic by keyword, else first topic
      const re = new RegExp(kw, 'i');
      const topic = topics.find(t => re.test(t.name)) || topics[0];
      if (!topic) continue;
      // shuffle options deterministically, track correct id
      const ids = ['a', 'b', 'c', 'd'];
      const withFlag = opts.map((text, i) => ({ text, correct: i === correctIdx }));
      const shuffled = rngShuffle(withFlag, extId.split('').reduce((h, c) => (h * 31 + c.charCodeAt(0)) | 0, 7));
      const options = shuffled.map((o, i) => ({ id: ids[i], text: o.text }));
      const correctAnswer = ids[shuffled.findIndex(o => o.correct)];
      await prisma.question.create({
        data: {
          externalId: extId,
          subjectId: subject.id, topicId: topic.id, subtopicId: null,
          type: 'SINGLE_CHOICE', part: 'A', difficulty: diff || 1,
          content,
          options: JSON.stringify(options),
          correctAnswer,
          explanation: expl,
          hints: JSON.stringify({ small: [expl], detailed: [], stepby: [] }),
          tags: JSON.stringify(tagsFrom(content)),
          year: 2027, source: 'Авторские задания', status: 'ACTIVE',
          timesSolved: 0, timesCorrect: 0,
        },
      });
      created++; perSubject[slug]++;
    }
  }

  // resync subject counters
  for (const slug of Object.keys(BANK)) {
    const s = await prisma.subject.findUnique({ where: { slug } });
    if (!s) continue;
    const qc = await prisma.question.count({ where: { subjectId: s.id, status: 'ACTIVE' } });
    await prisma.subject.update({ where: { id: s.id }, data: { questionsCount: qc } });
  }

  console.log('Humanities questions created:', created);
  console.log('Per subject:', JSON.stringify(perSubject));
  if (skippedNoSubject) console.log('Subjects not found:', skippedNoSubject);
}
main().catch(e => { console.error(e); process.exit(1); }).finally(() => prisma.$disconnect());
